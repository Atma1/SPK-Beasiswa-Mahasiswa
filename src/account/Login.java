    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Tangkap teks dari textfield dan passwordfield
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        Connection conn = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM account WHERE username = ? AND password = ?";

        try {
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, username);
            pstmt.setString(2, password);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                int idAccount = rs.getInt("id");
                String role = rs.getString("role");
                JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + role + "!");

                // Log idAccount untuk memastikan idAccount yang valid
                System.out.println("idAccount: " + idAccount);

                if (role.equals("Mahasiswa")) {
                    HomeMahasiswa homeMahasiswa = new HomeMahasiswa(idAccount);
                    homeMahasiswa.setVisible(true);
                } else if (role.equals("Admin")) {
                    Home home = new Home();
                    home.setVisible(true);
                }
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username atau password salah!");
            }

            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Login gagal: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

